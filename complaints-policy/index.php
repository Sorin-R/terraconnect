            <h1 class="inline-style-2">Complaints &amp; Dispute Resolution</h1>
            <p class="inline-style-3"><strong>Last Updated:</strong> April 2026</p>

            <h2 class="inline-style-4">1. Our Commitment</h2>
            <p>Terra Connect Ltd takes all complaints seriously and aims to resolve them quickly and fairly. We are committed to treating every complaint as an opportunity to improve our services. This policy applies to complaints from both consumers and business clients about any aspect of our services, website, or conduct.</p>

            <h2 class="inline-style-4">2. What Counts as a Complaint</h2>
            <p>A complaint is any expression of dissatisfaction with our services, communication, staff, or policies, whether received verbally or in writing. This includes:</p>
            <ul>
                <li>Dissatisfaction with the quality of work delivered</li>
                <li>Missed deadlines or poor communication</li>
                <li>Billing or payment disputes</li>
                <li>Data protection or privacy concerns</li>
                <li>Accessibility issues with our website or services</li>
                <li>Any other concern about how we have treated you</li>
            </ul>

            <h2 class="inline-style-4">3. How to Raise a Complaint</h2>
            <p>You can submit a complaint via any of the following channels:</p>
            <ul>
                <li><strong>Email:</strong> <a href="mailto:hello@terraconnect.co.uk" class="inline-style-5">hello@terraconnect.co.uk</a> &mdash; subject line: <em>&ldquo;Formal Complaint &ndash; [Brief Description]&rdquo;</em></li>
                <li><strong>Post:</strong> Terra Connect Ltd, 57 Tonbridge Drive, Basildon, SS15 6ND, United Kingdom</li>
                <li><strong>Online:</strong> Via our <a href="/contacts/" class="inline-style-5">contact form</a></li>
            </ul>
            <p class="inline-style-6">To help us resolve your complaint quickly, please include:</p>
            <ul>
                <li>Your name and contact details</li>
                <li>A clear description of your complaint</li>
                <li>Any relevant dates, reference numbers, or attachments</li>
                <li>What outcome you are seeking</li>
            </ul>

            <h2 class="inline-style-4">4. Our Complaints Process</h2>
            <div class="step-row"><span class="step-number">1</span><div class="step-content"><strong>Acknowledgement</strong> &mdash; We will acknowledge your complaint within <strong>2 business days</strong> of receipt. We will confirm who is handling it and provide a reference number.</div></div>
            <div class="step-row"><span class="step-number">2</span><div class="step-content"><strong>Investigation</strong> &mdash; We will investigate your complaint thoroughly and impartially. We may contact you to request further information during this stage.</div></div>
            <div class="step-row"><span class="step-number">3</span><div class="step-content"><strong>Resolution</strong> &mdash; We aim to provide a full written response within <strong>10 business days</strong> of receiving your complaint. If the complaint is complex and requires more time, we will update you within 10 business days and give you a revised timeframe (maximum 8 weeks).</div></div>
            <div class="step-row"><span class="step-number">4</span><div class="step-content"><strong>Outcome</strong> &mdash; Our response will explain our findings, what we propose to do, and how to escalate if you remain dissatisfied.</div></div>

            <h2 class="inline-style-4">5. Data Protection Complaints</h2>
            <p>If your complaint relates to how we handle your personal data, please contact our data protection contact directly:</p>
            <p class="inline-style-6"><strong>Email:</strong> <a href="mailto:privacy@terraconnect.co.uk" class="inline-style-5">privacy@terraconnect.co.uk</a></p>
            <p class="inline-style-6">If you are not satisfied with our response, you have the right to escalate to the <strong>Information Commissioner&rsquo;s Office (ICO)</strong>:</p>
            <p class="inline-style-7">
                Website: <a href="https://ico.org.uk/make-a-complaint/" rel="noopener noreferrer" target="_blank" class="inline-style-5">ico.org.uk/make-a-complaint</a><br />
                Helpline: 0303 123 1113
            </p>

            <h2 class="inline-style-4">6. Dispute Resolution</h2>
            <p>If you are not satisfied with the outcome of our internal complaints process, you may seek resolution through the following routes:</p>

            <h3 class="inline-style-8">a) Mediation</h3>
            <p>We are willing to engage in independent mediation as an alternative to litigation. We recommend the <strong>Centre for Effective Dispute Resolution (CEDR)</strong> at <a href="https://www.cedr.com/" rel="noopener noreferrer" target="_blank" class="inline-style-5">cedr.com</a> as a suitable independent mediator.</p>

            <h3 class="inline-style-8">b) Small Claims Court</h3>
            <p>For consumer disputes under &pound;10,000, you may submit a claim to the UK <strong>Small Claims Court</strong> (part of the County Court). This is an accessible, low-cost process. See <a href="https://www.gov.uk/make-court-claim-for-money" rel="noopener noreferrer" target="_blank" class="inline-style-5">gov.uk/make-court-claim-for-money</a> for details.</p>

            <h3 class="inline-style-8">c) Online Dispute Resolution (EU Clients)</h3>
            <p>If you are based in the EU, you may use the European Commission&rsquo;s Online Dispute Resolution platform: <a href="https://ec.europa.eu/consumers/odr/" rel="noopener noreferrer" target="_blank" class="inline-style-5">ec.europa.eu/consumers/odr</a>.</p>

            <h3 class="inline-style-8">d) Legal Action</h3>
            <p>Nothing in this policy prevents you or us from pursuing legal remedies through the courts of England and Wales, as specified in our <a href="/terms-of-service/" class="inline-style-5">Terms of Service</a>.</p>

            <h2 class="inline-style-4">7. Accessibility of This Process</h2>
            <p>We are committed to making our complaints process accessible to everyone. If you need this information in an alternative format, or need support to raise a complaint due to a disability, please contact us and we will do our best to assist.</p>

