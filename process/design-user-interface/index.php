                    <div class="highlight-box">
                        <h3>Accessibility &amp; Inclusive Design</h3>
                        <p>We design for everyone. Our <strong>user experience design</strong> practice adheres to WCAG 2.1 AA guidelines as a minimum standard. We ensure sufficient colour contrast, logical focus order, meaningful alt text, and keyboard navigability throughout every interface we create. Inclusive design is not a checkbox — it is a core principle that makes your product better for all users, including those with visual, motor, cognitive, or auditory impairments.</p>
                    </div>
