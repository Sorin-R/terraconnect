            <div class="highlight-box">
                <p><strong>If you are a consumer</strong> (an individual acting outside the course of a business), you have a statutory right to cancel your contract with us within <strong>14 calendar days</strong> of the date the contract is formed, without giving any reason.</p>
            </div>

