            <h2 class="inline-style-4">1. Overview</h2>
            <p>This policy sets out your rights to cancel a service agreement with Terra Connect Ltd and the process for doing so. It applies to both <strong>consumer clients</strong> (individuals contracting outside a business context) and <strong>business clients</strong>. Statutory rights may differ between these groups as outlined below.</p>
