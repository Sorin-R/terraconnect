            <h3 class="inline-style-6">c) Effect of Cancellation</h3>
            <p>If you cancel within the 14-day period:</p>
            <ul>
                <li>We will reimburse all payments received from you as soon as possible, and no later than <strong>14 days</strong> after the day we receive your cancellation notice</li>
                <li>Reimbursement will be made using the same means of payment used for the initial transaction, unless you have expressly agreed otherwise</li>
            </ul>

            <h3 class="inline-style-6">d) Exceptions to the Right to Cancel</h3>
            <p>The 14-day cancellation right does <strong>not apply</strong> where:</p>
            <ul>
                <li>You have <strong>expressly requested</strong> that we begin providing the service within the 14-day cancellation period <strong>and</strong> the service has been fully performed &mdash; in this case, you lose the right to cancel once the service is complete</li>
                <li>The service is <strong>fully bespoke</strong> (custom-built to your specifications) and work has commenced with your consent</li>
                <li>You are contracting in the course of a business (business-to-business contracts are governed by Section 3 below)</li>
            </ul>
            <p class="inline-style-5">Where we begin work at your request during the cancellation period, you agree to pay a proportionate amount for work completed up to the point of cancellation.</p>

